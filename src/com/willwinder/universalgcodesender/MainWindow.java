    private boolean openCommConnection() {
        boolean connected = false;
        try {
            this.clearTable();
            this.sentRowsValueLabel.setText("0");
            this.sentRows = 0;

            String port = commPortComboBox.getSelectedItem().toString();
            int portRate = Integer.parseInt(baudrateSelectionComboBox.getSelectedItem().toString());
             
            connected = controller.openCommPort(port, portRate);

        } catch (PortInUseException e) {
            this.displayErrorDialog("Error opening connection ("+ e.getClass().getName() + "): "+e.getMessage()+
                    "\n\nMac and Linux users, if this port is not in use please create a" +
                    "\ndirectory named \"/var/lock\" with the following commands:" +
                    "\n     sudo mkdir /var/lock" +
                    "\n     sudo chmod 777 /var/lock");
        }catch (Exception e) {
            e.printStackTrace();
            this.displayErrorDialog("Error opening connection ("+ e.getClass().getName() + "): "+e.getMessage());
        }
        return connected;
    }
    
    private void closeCommConnection() {
        this.controller.closeCommPort();
    }
    
