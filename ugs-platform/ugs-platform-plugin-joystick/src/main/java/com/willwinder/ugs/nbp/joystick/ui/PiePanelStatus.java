package com.willwinder.ugs.nbp.joystick.ui;

import javax.swing.JPanel;
import java.awt.Color;
import java.awt.Dimension;
import java.awt.Graphics;
import java.awt.Graphics2D;
import java.awt.RenderingHints;
import java.awt.geom.Ellipse2D;

public class PiePanelStatus extends JPanel {
    private final Color activeColor;
    private float value;

    public PiePanelStatus(int diameter, Color activeColor) {
        this.activeColor = activeColor;
        setMinimumSize(new Dimension(diameter, diameter));
        setPreferredSize(new Dimension(diameter, diameter));
    }

    @Override
    public void paint(Graphics g) {
        if (value == 0) {
            return;
        }

        Graphics2D g2 = (Graphics2D) g;
        g2.setRenderingHint(RenderingHints.KEY_ALPHA_INTERPOLATION, RenderingHints.VALUE_ALPHA_INTERPOLATION_QUALITY);
        g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
        g2.setRenderingHint(RenderingHints.KEY_COLOR_RENDERING, RenderingHints.VALUE_COLOR_RENDER_QUALITY);
        g2.setRenderingHint(RenderingHints.KEY_DITHERING, RenderingHints.VALUE_DITHER_ENABLE);
        g2.setRenderingHint(RenderingHints.KEY_FRACTIONALMETRICS, RenderingHints.VALUE_FRACTIONALMETRICS_ON);
        g2.setRenderingHint(RenderingHints.KEY_INTERPOLATION, RenderingHints.VALUE_INTERPOLATION_BILINEAR);
        g2.setRenderingHint(RenderingHints.KEY_RENDERING, RenderingHints.VALUE_RENDER_QUALITY);
        g2.setRenderingHint(RenderingHints.KEY_STROKE_CONTROL, RenderingHints.VALUE_STROKE_PURE);
        g2.setColor(activeColor);
        g2.draw(new Ellipse2D.Double(0.5, 0.5, getWidth() - 1d, getHeight() - 1d));

        g2.fillArc(0, 0, getWidth(), getHeight(), 270, Math.max(Math.round(360 * Math.abs(value)), 45));
    }

    public void setValue(float value) {
        this.value = value;
        invalidate();
    }
}
